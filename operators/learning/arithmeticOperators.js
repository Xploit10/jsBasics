
let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
// Mod
console.log(x % y);
// X to power y 
console.log(x ** y);

// Increment After
console.log(x++);
console.log(x);
// Increment Before
console.log(++x);

// Decrement After
console.log(x--);
console.log(x);

// Decrement Before
console.log(--x);
